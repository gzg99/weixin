package com.yq.entity;

/**
 * 装修样板
 * */
public class DecorationEntity {

	private Long id;
	
	private String companyName;
	
	private String companyImg;
	
	private String companyIntrl;
	
	private String companyDetail;
	
	private String addTime;
	
	private String type;
	
	private String isFineQuality;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getCompanyImg() {
		return companyImg;
	}

	public void setCompanyImg(String companyImg) {
		this.companyImg = companyImg;
	}

	public String getCompanyIntrl() {
		return companyIntrl;
	}

	public void setCompanyIntrl(String companyIntrl) {
		this.companyIntrl = companyIntrl;
	}

	public String getCompanyDetail() {
		return companyDetail;
	}

	public void setCompanyDetail(String companyDetail) {
		this.companyDetail = companyDetail;
	}

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getIsFineQuality() {
		return isFineQuality;
	}

	public void setIsFineQuality(String isFineQuality) {
		this.isFineQuality = isFineQuality;
	}
	
}
