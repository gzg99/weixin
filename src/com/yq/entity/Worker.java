package com.yq.entity;

public class Worker {

	private Long id;
	
	private String name;
	
	private int age;
	
	private String longitude;
	
	private String latitude;
	
	private String openId;
	
	private Long telPhone;
	
	private String credentials;
	
	private String credentialsImg;
	
	private String workerId;
	
	private String workerImg;
	
	private String workerIntro;
	
	private int isVip;
	
	private String addTime;
	
	private String updateTime;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getOpenId() {
		return openId;
	}

	public void setOpenId(String openId) {
		this.openId = openId;
	}

	public Long getTelPhone() {
		return telPhone;
	}

	public void setTelPhone(Long telPhone) {
		this.telPhone = telPhone;
	}

	public String getCredentials() {
		return credentials;
	}

	public void setCredentials(String credentials) {
		this.credentials = credentials;
	}

	public String getCredentialsImg() {
		return credentialsImg;
	}

	public void setCredentialsImg(String credentialsImg) {
		this.credentialsImg = credentialsImg;
	}

	public String getWorkerId() {
		return workerId;
	}

	public void setWorkerId(String workerId) {
		this.workerId = workerId;
	}

	public String getWorkerImg() {
		return workerImg;
	}

	public void setWorkerImg(String workerImg) {
		this.workerImg = workerImg;
	}

	public String getWorkerIntro() {
		return workerIntro;
	}

	public void setWorkerIntro(String workerIntro) {
		this.workerIntro = workerIntro;
	}

	public int getIsVip() {
		return isVip;
	}

	public void setIsVip(int isVip) {
		this.isVip = isVip;
	}

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	
}
